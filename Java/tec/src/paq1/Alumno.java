package paq1;

public class Alumno 
{
	private long cuenta;
	private String nombre;
	private double beca;
	
	public long getCuenta()
	{
		return cuenta;
	}
	
	public void setCuenta(long cuenta)
	{
		this.cuenta = cuenta;
	}
	
	public String getNombre()
	{
		return nombre;
	}
	
	public void setNombre(String nombre)
	{
		this.nombre = nombre;
	}
	
	public double getBeca()
	{
		return beca;
	}
	
	public void setBeca(double beca)
	{
		this.beca = beca;
	}
	
		
	
}
